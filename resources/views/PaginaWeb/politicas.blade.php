                personas a conocer, actualizar, y rectificar de forma gratuita la información que recaude sobre ellas en
                bases de datos personales tratados por La CONFEDERACIÓN DE GUÍAS DE TURISMO DE
                COLOMBIA -CONFEGUIAS.</p>
            <u><strong>Tratamiento y finalidad de los datos personales</strong></u>
            <p>La CONFEDERACIÓN DE GUÍAS DE TURISMO DE COLOMBIA -CONFEGUIAS- utiliza la información personal que
                los titulares suministran con el fin de desarrollar sus servicios, elaborar estadísticas, enviar
                información de noticias, actividades, entre otros relacionados con el objeto social de la Asociación.
            </p>
            <p>Esta política aplica a todos los datos personales que CONFEGUIAS recolecta, almacena, usa, administra,
                transfiere y transmite, tales como datos de afiliados, proveedores, contratistas, empleados y asistentes
                a eventos.</p>
            <p>La Asociación se compromete a no recabar información innecesaria y a mantener la confidencialidad y
                seguridad de la misma.
            </p>
            <p>El Responsable del tratamiento de los datos será La CONFEDERACIÓN DE GUÍAS DE TURISMO DE COLOMBIA
                -CONFEGUIAS.</p>
