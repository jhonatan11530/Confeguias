            <u><strong>Procedimiento para el ejercicio del derecho de habeas data:</strong></u>
            <p>En cumplimiento de las normas sobre protección de datos personales La CONFEDERACIÓN DE GUÍAS DE TURISMO
                DE COLOMBIA -CONFEGUIAS- presenta el procedimiento y requisitos mínimos para el ejercicio de sus
                derechos:</p>
            <P>Los titulares, sus causahabientes, el representante y/o apoderado o por estipulación a favor de otro,
                podrán consultar la información personal del titular que repose en cualquier base de datos.</P>
            <P>Para la radicación y atención de su solicitud le solicitamos suministrar la siguiente información:</P>
            <ul>
                <li>Nombre completo y apellidos.</li>
                <li>Datos de contacto (Dirección física y/o electrónica y teléfonos de contacto).</li>
                <li>Medios para recibir respuesta a su solicitud.</li>
                <li>Motivo(s)/hecho(s) que dan lugar al reclamo con una breve descripción del derecho que desea ejercer
                    (conocer, actualizar, rectificar, solicitar prueba de la autorización otorgada, revocarla, suprimir,
                    acceder a la información) Firma (si aplica) y número de identificación.</li>
            </ul>
            <p>La consulta será atendida en un término máximo de diez (10) días hábiles contados a partir de la fecha de
                recibo de la misma. Cuando no fuere posible atender la consulta dentro de dicho término, se informará al
                interesado, expresando los motivos de la demora y señalando la fecha en que se atenderá su consulta, la
                cual en ningún caso superará los cinco (5) días hábiles siguientes al vencimiento del primer término.
            </p>
            <p>Cuando el titular o sus causahabientes realicen un reclamo por considerar que la información contenida en
                una base de datos debe ser objeto de corrección, actualización o supresión, o cuando adviertan un
                presunto incumplimiento de la Ley, podrán presentar un reclamo, el cual deberá contener la siguiente
                información:</p>
            <ul>
                <li>Identificación del titular, del Causahabiente o del apoderado.</li>
                <li>La descripción de los hechos que dan lugar al reclamo.</li>
                <li>La dirección.</li>
                <li>Los documentos que se quiera hacer valer.</li>
            </ul>
            <p>Si el reclamo resulta incompleto, se requerirá al interesado dentro de los cinco (5) días siguientes a la
                recepción del reclamo para que subsane las fallas. Transcurridos dos (2) meses desde la fecha del
                requerimiento, sin que el solicitante presente la información requerida, se entenderá que ha desistido
                del reclamo.</p>
            <p>El término máximo para atender el reclamo será de quince (15) días hábiles contados a partir del día
                siguiente a la fecha de su recibo. Cuando no fuere posible atender el reclamo dentro de dicho término,
                se informará al interesado los motivos de la demora y la fecha en que se atenderá su reclamo, la cual no
                superará los ocho (8) días hábiles siguientes al vencimiento del primer término.</p>
            <p>Una vez cumplidos los términos señalados por la Ley 1581 de 2012 y las demás normas que la reglamenten o
                complementen, el Titular al que se deniegue, total o parcialmente, el ejercicio de los derechos de
                acceso, actualización, rectificación, supresión y revocación, podrá poner su caso en conocimiento de la
                Superintendencia de Industria y Comercio –Delegatura para la Protección de Datos Personales-.</p>
